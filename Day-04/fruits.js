let fruits = ["apple", "banana", "orange"];
console.log(fruits);
console.log(fruits[0]);
console.log(fruits.push("grape")); // Adding an element at the end
console.log(fruits.pop()); // Removing the last element
console.log(fruits.shift()); // Removing the first element
console.log(fruits.unshift("kiwi")); // Adding an element at the beginning
console.log(fruits.length); // Getting the length of the array
console.log(fruits.indexOf("banana")); // Finding the index of an element
console.log(fruits.includes("orange")); // Checking if an element exists in the array
console.log(fruits.reverse()); // Reversing the array
console.log(fruits.sort()); // Sorting the array
console.log(fruits.slice(1, 3)); // Slicing the array
console.log(fruits.forEach((fruit) => console.log(fruit))); // Iterating over array elements
console.log(fruits.map((fruit) => fruit.toUpperCase())); // Mapping array elements to a new array
console.log(fruits.filter((fruit) => fruit.length > 5)); // Filtering array elements



