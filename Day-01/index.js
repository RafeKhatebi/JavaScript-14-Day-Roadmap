/**  This is a simple JavaScript program to demonstrate variable declaration
 and outputting values to the console.*/

// Declaring variables using var
var city = "Herat";
console.log("City: " + city);

// Declaring variables using let
let name1 = "jamil jamili";
let age = 25;
console.log("Name: " + name1);
console.log("Age: " + age);

// Declaring variables using const
const country = "Afghanistan";
console.log("Country: " + country);

//Declaring arithmetic variables
let num1 = 10;
let num2 = 5;
let sum = num1 + num2;
console.log("Sum: " + sum);

